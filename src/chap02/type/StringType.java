package chap02.type;

public class StringType {

	public static void main(String[] args) {
		// String: 문자열 저장   / ""열    ''문자하나
		
		String carName;
		carName = "Kiaaa";
		
		System.out.println(carName);
		System.out.println("");
		System.out.println("여러문자열..");
		System.out.println("큰따옴표\" 작은따옴표'");  // 작따는 그냥써도됨
	}

}
